const ids = [];
let fails = 0;
for (let i = 0; i < slots.length; i++) {
  const [epVal, sizeVal, mvMin, mvMax] = slots[i];
  const p = pick(epVal, sizeVal, mvMin, mvMax);
  if (!p) {
    console.error(`#${i + 1}: 在庫切れ ep=${epVal} ${sizeVal}x${sizeVal} ${mvMin}-${mvMax}手`);
    fails++;
    ids.push(-1);
  } else {
    ids.push(p.id);
  }
}

if (fails) {
  console.error(`${fails}問が在庫不足で未割当`);
  process.exit(1);
}

